#include <stdlib.h>

int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int nb;

	nb = 0;
	i = 0;
	while (tab[i] != NULL)
	{
		if (f(tab[i]) == 1)
			nb++;
		i++;
	}
	return (nb);
}
